def hindex(s: str, n: int) -> int:
    return int(hashlib.md5(s.encode()).hexdigest(), 16) % n


def city_name(row: dict) -> str:
    zh = (row.get("zh") or "").strip()
    if zh:
        return zh
    return row["city"]


def clean_airport(name: str) -> str:
    name = name.replace("\\\\'", "'").replace("\\'", "'")
    name = re.sub(r"\s+", " ", name).strip()
    return name


def make_tag(row: dict, place: str, country_zh: str, continent_zh: str) -> str:
    templates = [
        f"{country_zh}航图上的安静坐标",
        f"被航线轻轻点亮的{place}",
        f"{continent_zh}腹地里的起飞点",
        f"公路到不了就飞到的{place}",
        f"{country_zh}支线网上的一枚铆钉",
        f"地图边缘也能登机的{place}",
        f"用跑道证明存在的{place}",
        f"{country_zh}省会光环之外的{place}",
        f"短跑道上的{place}",
        f"{continent_zh}航线末梢的{place}",
    ]
    # 岛屿/港/堡等微调
    city = row["city"].lower()
    if any(k in city for k in ("island", "isle", "atoll", "cay")):
        templates.append(f"大洋里一座可降落的{place}")
    if any(k in city for k in ("bay", "port", "harbour", "harbor")):
        templates.append(f"港口背后的空中入口·{place}")
    tag = templates[hindex(row["iata"] + "tag", len(templates))]
    # 控制长度 8-20
    if len(tag) > 20:
        tag = tag[:20]
    if len(tag) < 8:
        tag = f"{country_zh}的{place}"
        if len(tag) < 8:
            tag = f"{continent_zh}小城{place}"
    return tag


def make_body(row: dict) -> tuple[str, str]:
    place = city_name(row)
    cc = row.get("cc") or ""
    country_en = row.get("country") or ""
    country_zh, hook = COUNTRY.get(cc, (country_en or "当地", "在更大枢纽的阴影里，它用有限的航班维持自己的对外节奏"))
    continent_zh = CONTINENT_LABEL.get(row.get("continent") or "", "世界")
    airport = clean_airport(row.get("airport") or "")
    deg = int(row.get("degree") or 0)
    dests = (row.get("dests") or "").split(";")
    dests = [d for d in dests if d]
    dest_hint = ""
    if dests:
        # "Tokyo(NRT)" -> 取城市名
        first = dests[0]
        m = re.match(r"(.+)\(([A-Z0-9]{3})\)$", first)
        if m:
            dest_hint = f"现有航线可衔接{m.group(1)}"
        else:
            dest_hint = f"现有航线可衔接{first}"

    tag = make_tag(row, place, country_zh, continent_zh)

    roles = [
        f"{place}是{country_zh}的一座地方城市，行政与日常节奏都更贴近本地而非首都叙事。",
        f"{place}位于{country_zh}，在全国城市等级里并不靠前，却实实在在承担着区域进出港功能。",
        f"{place}是{country_zh}{continent_zh}航线网络上的一个基层节点，服务半径以周边城镇为主。",
    ]
    pos = roles[hindex(row["iata"] + "pos", len(roles))]

    contrasts = [
        f"人们容易把目光放在大都会枢纽上，但{hook}——{place}正是这种逻辑的具体落点。",
        f"它很少出现在旅行清单封面，可对本地人而言，起飞往往比驱车穿越{continent_zh}腹地更现实。{hook}。",
        f"名气不大并不等于无足轻重：{hook}，而{place}把这种需要收进一条条时刻表。",
    ]
    mid = contrasts[hindex(row["iata"] + "mid", len(contrasts))]

    facts = []
    if airport:
        facts.append(f"对应机场为{airport}（{row['iata']}）")
    if deg > 0:
        facts.append(f"当前直飞连通度约{deg}条")
    elif deg == 0:
        facts.append("更多作为被飞抵或季节性通航的节点存在")
    if dest_hint:
        facts.append(dest_hint)
    end = "；".join(facts[:2]) + "。"

    body = pos + mid + end
    # 压缩到约 120-220 字
    if len(body) > 220:
        body = body[:217] + "。"
    if len(body) < 120:
        body += f"{place}的航班或许不多，却足以把本地时间与外界对齐。"
        if len(body) > 220:
            body = body[:217] + "。"
    return tag, body


def main():
    src = os.path.join(ROOT, "tools", "out", "bio_batches", "tail.json")
    with open(src) as f:
        rows = json.load(f)
    out = {}
    for row in rows:
        tag, body = make_body(row)
        out[row["iata"]] = {"tag": tag, "body": body}
    path = os.path.join(ROOT, "data", "bios", "bios_tail.json")
    with open(path, "w") as f:
        json.dump(out, f, ensure_ascii=False, indent=1)
    lens = sorted(len(b["body"]) for b in out.values())
    print(f"tail bios={len(out)} body min={lens[0]} med={lens[len(lens)//2]} max={lens[-1]}")
    print(f"-> {path}")


if __name__ == "__main__":
    main()
